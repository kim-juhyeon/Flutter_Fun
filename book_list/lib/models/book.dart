class Book {
  final String title;
  final String subtitle;
  final String description;
  final String image;
  Book({
    required this.title,
    required this.subtitle,
    required this.description,
    required this.image,
  });
}
/*final 키워드는 변수가 초기화된 후 값을 변경할 수 없음을 나타내는 키워드입니다.
required 키워드는 해당 매개변수가 반드시 전달되어야 함을 나타내는 키워드입니다.*/
